#include <iostream>;
#include <iomanip>;
#include <fstream>;

using namespace std;
/*
 * Calculates the total savings a person would have after 45 years,
 * with 4.5%, 5.0% , and 5.5% interest applied monthly.
 * Putting in $100.00 a month increased by 10% every year.
 * Finally, writing the data to a file.
 *
 * @author Dylan Connor
 */
void main()
{
	//Variables
	ofstream outFile;
	float deposit = 100;
	float balance = 0;
	float balance2 = 0;
	float balance3 = 0;
	float interestRatePerMonth = 0.045 / 12;  //4.5% interest
	float interestRatePerMonth2 = 0.05 / 12;  //5.0% interest
	float interestRatePerMonth3 = 0.055 / 12; //5.5% interest
	int totalNumberOfMonths = 12*45;
	int years = 1; //Count how many years passed
	int realYear = 2018; //Show actual years starting at 2018

	outFile.open("outputData.txt");
	outFile << "---------------------------------------------------------------------------" << endl;
	outFile << "| Number | Year |   Monthly  | Balance on the following market interests  |" << endl;
	outFile << "|of years|      |Contribution|     4.5%     |     5.0%     |     5.5%     |" << endl;
	outFile << "---------------------------------------------------------------------------" << endl;
	//Loop to calculate balance over 45 years
	for (int i = 0; i < totalNumberOfMonths; ++i)
	{
		//Calculate total balance for each interest rate separately
		balance = (balance + balance*interestRatePerMonth) + deposit;
		balance2 = (balance2 + balance2*interestRatePerMonth) + deposit;
		balance3 = (balance3 + balance3*interestRatePerMonth) + deposit;
		//Show balance after each year instead of after each month
		if (i / 12 == years)
		{
			deposit = (deposit *.10) + deposit;
			outFile << fixed << setprecision(2) << left << setw(1)<< "|" << setw(8) << years << setw(1) << "|" << setw(6) << realYear;
			outFile << setw(1) << "|" << setw(12) << deposit << setw(2) << "|$" << setw(13) << balance << setw(2) << "|$";
		    outFile << setw(13) << balance2 << setw(2) << "|$" << setw(13) << balance3 << setw(1) << "|" << endl;
			outFile << "---------------------------------------------------------------------------" << endl;
			++years;
			++realYear;
		}
	}
	//Final output after year 45
	if (years == 45)
	{
		balance = (balance + balance*interestRatePerMonth) + deposit;
		balance2 = (balance2 + balance2*interestRatePerMonth) + deposit;
		balance3 = (balance3 + balance3*interestRatePerMonth) + deposit;
		outFile << fixed << setprecision(2) << left << setw(1) << "|" << setw(8) << years << setw(1) << "|" << setw(6) << realYear;
		outFile << setw(1) << "|" << setw(12) << deposit << setw(2) << "|$" << setw(13) << balance << setw(2) << "|$";
		outFile << setw(13) << balance2 << setw(2) << "|$" << setw(13) << balance3 << setw(1) << "|" << endl;
		outFile << "---------------------------------------------------------------------------" << endl;
	}
	//Close file
	outFile.close();
	//Give user a sign that the program actually ran at least some of its code
	system("pause");
}
